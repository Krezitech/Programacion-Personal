// "Let" pued crear un valor e inicializar una variable

let producto = "tablet";

// También puede reasignar el valor igual que "var"

producto = "Monitoreo";
console.log(producto);

producto = "500";
console.log(producto);

producto = false;
console.log(producto);

// Puede crearse una variable con "let" sin valor y NO tendrá errores (Da "undefined" como resultado)

let precio;
console.log(precio);
